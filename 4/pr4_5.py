A = set(input("Введіть символи множини A без пробілів (наприклад: abcde): "))

x = input("Введіть символ x: ")

B = A.copy()

if x in B:
    B.remove(x)
    print(f"Символ '{x}' був у множині A, тому його видалено.")
else:
    B.add(x)
    print(f"Символ '{x}' відсутній у множині A, тому його додано.")

print("Множина A:", A)
print("Множина B:", B)
